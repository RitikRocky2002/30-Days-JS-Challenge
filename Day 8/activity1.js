// Task 1 : Use template literals to create string that includes variables for a person's name and age, and log the string to the console

let username = "Ritik";
let age = 21;

let str = `Name is ${username} and age is ${age}`;
console.log(str); //Output : Name is Ritik and age is 21

// Task 2 : Create a multi-line string template literals anf log it to the console

let str1 = `Username is ${username}
Age is ${age}`;
console.log(str1);

// Output :
// Username is Ritik
// Age is 21
